my_dict= {'apple': 1, 'banana': 2, 'cherry': 3}
for key in my_dict:
    print(key)

#printing values
my_dict= {'apple': 1, 'banana': 2, 'cherry': 3}
for value in my_dict.values():
    print(value)

#printing both keys and values
my_dict= {'apple': 1, 'banana': 2, 'cherry': 3}
for key, value in my_dict.items():
    print(key,":",value)


