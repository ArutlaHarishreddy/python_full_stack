#Calculator
def add(a,b):
    result=a+b
    print(result)
def substract(a,b):
    result=a-b
    print(result)
def multiply(a,b):
    result=a*b
    print(result)
def divide(a,b):
    if b==0:
        print("Error: Division by zero is not allowed")
    else:
        result=a/b
        print(result)
def square(a):
    result=a*a
    print(result)
def cube(a):
    result=a*a*a
    print(result)
    

